# Profundizando en los sistemas de numeraci√≥n

# Sistema decimal
a = 10
print(f'El valor de a es: {a}')
print(f'El tipo de dato decimal de a es: {type(a)}')

# Sistema binario
b = 0b1010
print(f'El valor de b es: {b}')
print(f'El tipo de dato binario de b es: {type(b)}')

# Sistema octal
c = 0o12
print(f'El valor de c es: {c}')
print(f'El tipo de dato octal de c es: {type(c)}')

# Sistema hexadecimal
d = 0xA
print(f'El valor de d es: {d}')
print(f'El tipo de dato hexadecimal de d es: {type(d)}')

# Valor decimal, base 10
e = int('23', 10)
print(f'El valor de e es: {e}')
print(f'El tipo de dato decimal de e es: {type(e)}')

# Valor binario, base 2
f = int('1010', 2)
print(f'El valor de f es: {f}')
print(f'El tipo de dato binario de f es: {type(f)}')

# Valor octal, base 8
g = int('12', 8)
print(f'El valor de g es: {g}')
print(f'El tipo de dato octal de g es: {type(g)}')

# Valor hexadecimal, base 16
h = int('A', 16)
print(f'El valor de h es: {h}')
print(f'El tipo de dato hexadecimal de h es: {type(h)}')

# Valor en base 5
i = int('1234', 5)
print(f'El valor de i es: {i}')
print(f'El tipo de dato en base 5 de i es: {type(i)}')